/** Talents "School" filter values -> stable substring in official programme school text (avoids e.g. "Engineering" matching Chemical). */
const SCHOOL_FILTER_SNIPPET = {
  "Arts, Design and Architecture": "school of arts, design and architecture",
  Business: "school of business",
  "Chemical Engineering": "school of chemical engineering",
  "Electrical Engineering": "school of electrical engineering",
  Engineering: "school of engineering",
  Science: "school of science",
};

/**
 * When a school filter is chosen, limit Program optgroups to programmes whose host school line matches.
 * Supports Talents short labels, full "School of …" names, and "Joint programmes".
 */
export function programmeMatchesSchoolFilter(programmeSchool, schoolFilter) {
  if (!schoolFilter || schoolFilter === "Other") return true;
  if (schoolFilter === "Joint programmes") {
    return programmeSchool.toLowerCase().includes("joint programme");
  }
  const needle = (SCHOOL_FILTER_SNIPPET[schoolFilter] || schoolFilter).trim().toLowerCase();
  return programmeSchool.toLowerCase().includes(needle);
}

