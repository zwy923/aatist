const PROFILE_VARCHAR_MAX = 255;

export function truncateProfileField(value, maxLen = PROFILE_VARCHAR_MAX) {
  if (value == null) return "";
  const s = String(value).trim();
  if (s.length <= maxLen) return s;
  return s.slice(0, Math.max(0, maxLen - 1)) + "\u2026";
}

export function getProgrammeByName(name) {
  const n = (name || "").trim();
  return AALTO_PROGRAMMES.find((p) => p.name === n) ?? null;
}

/** Maps selected degree programme to users.school / faculty / major (DB varchar 255). */
export function buildRegistrationAcademicFields(programme) {
  if (!programme) return null;
  const joint = programme.school.toLowerCase().includes("joint programme");
  return {
    school: truncateProfileField(programme.school),
    faculty: joint ? "Joint programme" : truncateProfileField(programme.school),
    major: truncateProfileField(programme.name),
  };
}
